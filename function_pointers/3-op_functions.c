/**
 * op_add - add
 * @num1: integer
 * @num2: integer
 * Return: num1 + num2
 */
int op_add(int num1, int num2)
{
	return (num1 + num2);
}

/**
 * op_sub - substract
 * @num1: integer
 * @num2: integer
 * Return: num1 - num2
 */
int op_sub(int num1, int num2)
{
	return (num1 - num2);
}

/**
 * op_mod - mod for remainder
 * @num1: integer
 * @num2: integer
 * Return: result
 */
int op_mod(int num1, int num2)
{
	return (num1 % num2);
}

/**
 * op_div - divide
 * @num1: integer
 * @num2: integer
 * Return: num1 / num2
 */

int op_div(int num1, int num2)
{
	return (num1 / num2);
}

/**
 * op_mul - multiply
 * @num1: integer
 * @num2: integer
 * Return: num1 * num2
 */
int op_mul(int num1, int num2)
{
	return (num1 * num2);
}
